int		paste_hash(char **tetri)
{
	int x;
	int y;

	y = -1;
	while (++y <= 4)
	{
		x = -1;
		while (++x <= 4)
		{
			if (tetri[y][x] == '#')
				if (tetri[y - 1][x] != '#' && tetri[y + 1][x] != '#' &&
						tetri[y][x - 1] != '#' && tetri[y][x + 1] != '#')
					return (1);
		}
	}
	return (0);
}
