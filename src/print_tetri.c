#include "libft.h"

void  print_tetri(char **tetri)
{
	int i;
	int	j;

	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
		{
			ft_putchar(tetri[i][j]);
		}
		ft_putchar('\n');
	}
}
