char	**replace(char **tetri)
{
	static int	a = 0;
	int			i;
	int			j;

	i = -1;	
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
		{
			if (tetri[i][j] == '#')
				tetri[i][j] = 'A' + a;
		}
	}
	a++;
	return (tetri);
}
